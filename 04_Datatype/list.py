# Lists
my_list = list(range(0, 101, 2))  # Create the list
print(my_list)

print(my_list[0:8])  # Printing the first ten elements of the list

print(len(my_list))  # Finding the length of the list

my_list.append("Hii")  # Appending a value to this list - can be any type!
print(my_list)

list = ['India', 'USA', 'Canada']
print(len(list))
list.append(['France', 'Spain'])
print(list)
print(len(list))
list.extend(['France', 'Spain'])
print(list)

